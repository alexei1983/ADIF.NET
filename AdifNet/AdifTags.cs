        /// Determines whether or not the specified string is an ADIF tag name.
        /// </summary>
        /// <param name="name">Name to check.</param>
        public static bool IsTagName(string name)
        {
            return GetTagNames().Any(c => c.Equals(name, StringComparison.OrdinalIgnoreCase));
        }

        /// <summary>
        /// Retrieves all ADIF QSO tag names.
        /// </summary>
        public static IEnumerable<string> GetQsoTagNames()
        {
            foreach (var fieldInfo in typeof(AdifTags).GetConstants(typeof(string)))
            {
                if (fieldInfo != null && fieldInfo.GetRawConstantValue() is string strValue)
                {
                    switch (strValue)
                    {
                        case CreatedTimestamp:
                        case UserDef:
                        case AppDef:
                        case AdifVer:
                        case ProgramVersion:
                        case EndHeader:
                        case ProgramId:
                            continue;
                        default:
                            yield return strValue;
                            break;
                    }
                }
            }
        }
    }
